"""
Арифметические операторы

Напишите программу, которая принимает два числа на вход 
и выполняет операции сложения, вычитания, умножения, деления и остатка от деления.
"""

def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def reminder(a, b):
    return a % b